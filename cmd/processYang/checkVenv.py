import sys

def getBasePrefix():
    return getattr(sys, "base_prefix", None) or getattr(sys, "real_prefix", 
                                                        None) or sys.prefix
if __name__ == "__main__":
    if getBasePrefix() != sys.prefix:
        print('true')
    else:
        print('false')
